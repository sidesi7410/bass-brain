// Haversine distance in miles
function haversine(lat1, lon1, lat2, lon2) {
  const R = 3959; // Earth's radius in miles
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLon = (lon2 - lon1) * Math.PI / 180;
  const a = Math.sin(dLat / 2) ** 2 +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
    Math.sin(dLon / 2) ** 2;
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
}

export function getSpotsByProximity(lat, lon, radiusMiles = 100) {
  return fishingSpots
    .map(spot => ({
      ...spot,
      distance: Math.round(haversine(lat, lon, spot.lat, spot.lon))
    }))
    .filter(spot => spot.distance <= radiusMiles)
    .sort((a, b) => a.distance - b.distance);
}

export function searchSpots(query) {
  const q = query.toLowerCase().trim();
  return fishingSpots.filter(spot =>
    spot.name.toLowerCase().includes(q) ||
    spot.state.toLowerCase() === q ||
    spot.notes.toLowerCase().includes(q)
  );
}

export function getAllSpots() {
  return fishingSpots;
}
